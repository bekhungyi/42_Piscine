int	ft_is_prime(int nb)
{
	int	n;
	int	p;

	n = 1;
	p = 0;
	while (n < nb)
	{
		if (nb % n == 0)
		{
			n++;
			p++;
		}
		else
			n++;
	}
	return (p);
	if (p == 2)
		return (1);
	else
		return (0);
}
